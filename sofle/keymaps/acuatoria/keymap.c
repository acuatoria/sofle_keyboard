#include "config.h"
#include "matrix.h"
#include "quantum.h"

#define ANIMATION_DELAY 50 // Milisegundos entre cada fotograma de la animación
#define MAX_DROPS 50

typedef struct {
    uint8_t x;
    uint8_t y;
    int8_t speed;
    char symbol;
} Drop;

Drop drops[MAX_DROPS];

void init_drops() {
    for (int i = 0; i < MAX_DROPS; i++) {
        drops[i].x = rand() % OLED_DISPLAY_WIDTH;
        drops[i].y = -(rand() % OLED_DISPLAY_HEIGHT);
        drops[i].speed = (rand() % 5) + 1;
        drops[i].symbol = '0' + (rand() % 10); // Dígito aleatorio
    }
}

void update_drop(Drop *drop) {
    drop->y += drop->speed;
    if (drop->y >= OLED_DISPLAY_HEIGHT)
        drop->y = -(rand() % OLED_DISPLAY_HEIGHT);
}

void draw_drop(Drop *drop) {
    oled_write_char(drop->x * 6, drop->y, drop->symbol, false);
}

void render_animation() {
    oled_clear();

    for (int i = 0; i < MAX_DROPS; i++) {
        update_drop(&drops[i]);
        draw_drop(&drops[i]);
    }

    oled_refresh();
}

void matrix_scan_user(void) {
    static uint32_t last_animation_time = 0;

    if (timer_elapsed(last_animation_time) > ANIMATION_DELAY) {
        render_animation();
        last_animation_time = timer_read();
    }
}

void matrix_init_user(void) {
    init_drops();
}

